#include "ft_printf.h"

int	ft_hexa_sm(unsigned int h)
{
	int		i;
	char	*num;

	num = "0123456789abcdef";
	i = 0;
	if (h >= 16)
		i += ft_hexa_sm(h / 16);
	ft_putchar(num[h % 16]);
	return (i + 1);
}
